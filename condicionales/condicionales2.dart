void main() {
  int dia = 12;
  switch (dia) {
    // entre parentesis recibe una variable o valor
    case 1:
      print("LUNES");
      break;
    case 2:
      print("MARTES");
      break;
    case 3:
      print("MIERCOLES");
      break;
    case 4:
      print("JUEVES");
      break;
    case 5:
      print("VIERNES");
      break;
    case 6:
      print("SABADO");
      break;
    case 7:
      print("DOMINGO");
      break;
    default:
      print("DIA INCORRECTO");
      break;
  }
}
